            <h1>2-Confederação Maçônica do Brasil – fundada em 06/04/1991.</h1>

            <p>Realizou-se em Brasília, nos dias 4, 5 e 6 de abril de 1991, a “XXXVª Assembleia Geral do Colégio de
                Presidentes”, tendo como local o Instituto Presbiteriano Nacional de Educação. [Boletim Oficial do GOSC
                n°310/91, de 2 de maio de 1990, p.19] A Reunião tinha como “Edital de Convocação” a votação da proposta
                de
                reforma do Estatuto e do Regimento Interno da Confederação Maçônica do Brasil – COMAB, ex-Colégio de
                Presidentes
                da Maçonaria Brasileira. Um dos pontos de maior evidência debatidos foi a discussão e votação do projeto
                da
                “Confederação Maçônica do Brasil”. Este projeto, após discutido, votado e apresentada sua redação final,
                foi
                aprovado por unanimidade. Já o Regimento Interno ficou para ser apresentado na Reunião a ser realizada
                em São
                Luiz/MA, entre os dias 13 e 16 de junho daquele ano. Essa alteração objetivou vincular cada Grande
                Oriente
                Federado a uma Confederação, sem perda da autonomia, independência e soberania, com as seguintes
                finalidades:a.
                Representar, de forma confederada, as Potências e Obediências filiadas, mediante deliberação específica,
                perante
                as organizações, de maçons do país e do estrangeiro, e perante o mundo profano, tendo em vista a
                grandeza, a
                harmonia e a glória da Maçonaria; b. Congregar as Obediências no estudo da Filosofia, História, Liturgia
                e
                Simbologia Maçônica, induzindo-as à prática da filantropia, do civismo, do desenvolvimento científico,
                cultural
                e artístico e ao aprimoramento moral das sociedades sob sua circunscrição; c. Colaborar com as
                autoridades
                legitimamente constituídas no sentido da ordem, do progresso e do bem-estar da população brasileira; d.
                Incrementar a difusão, pelas federadas, da doutrina e dos postulados da Maçonaria universal e do ideal
                maçônico;
                e. Estudar e coordenar medidas que possam interessar aso federados, no sentido da ação maçônica
                conjunta; f.
                Sugerir e estimular instruções maçônicas entre as federadas; g. Ativar as relações das federadas entre
                si, e
                destas, com outras Potências; h. Manter cursos no campo educativo, científico e assistencial,
                diretamente ou por
                intermédio das federadas; conceder bolsas de estudos como pessoa humana útil e produtiva à sociedade; e
                i.
                Manter, em sua sede, biblioteca que contenha departamento público e maçônico, e estimular a criação e o
                desenvolvimento de organismos similares pelas federadas. Assinaram e anuíram a esse novo Estatuto: –
                Grande
                Oriente Autônomo de Alagoas; – Grande Oriente do Estado do Amazonas; – Grande Oriente da Bahia; – Grande
                Oriente
                do Ceará; – Grande Oriente Autônomo do Maranhão; – Grande Oriente do Mato Grosso; – Grande Oriente de
                Mato
                Grosso do Sul; – Grande Oriente Minas Gerais; – Grande Oriente Paulista; – Grande Oriente Independente
                da
                Paraíba; – Grande Oriente do Paraná; – Grande Oriente Independente de Pernambuco; – Grande Oriente
                Independente
                do Piauí; – Grande Oriente Independente do Rio de Janeiro; – Grande Oriente Independente do Rio Grande
                do Norte;
                – Grande Oriente do Rio Grande do Sul; – Grande Oriente de Santa Catarina.
            </p>
