            <h1 style="margin-top:20px">História da COMAB</h1>
            <p>
                A Confederação Maçônica do Brasil (COMAB) é uma confederação de Obediências Maçônicas do Brasil.
                Reúne-se
                ordinariamente e anualmente, no mês de agosto, para tratar de assuntos de ordem interna, e de eleição
                para a sua
                Administração, que renova-se a cada ano, extraordinariamente tantas vezes quantas forem necessárias,
                assim como
                no mês de agosto, para a posse da sua nova Administração. Sua sede institucional é localizada em Belo
                Horizonte-MG. A COMAB foi fundada com o objetivo de agrupar e organizar os Grandes Orientes
                Independentes, que
                surgiram, principalmente, nas cisões ocorridas dentro dos Grandes Orientes Estaduais ligados ao Grande
                Oriente
                do Brasil (GOB) no ano de 1973. Sendo considerada Maçonaria Regular pois pratica todas as regras da
                maçonaria
                universal, bem como integra demias confederações internacionais e regulares pelo mundo. Possui grande
                representatividade em todo o território nacional, através dos Grandes Orientes Independentes de cada
                Estado do
                Brasil, para além de dar apoio na organização do grande contingente de maçons, ligados à estas Potências
                Maçônicas Estaduais, presente em 25 (vinte e cinco) estados Brasileiros, incluíndo o Distrito Federal.
            </p>

            <h1>1. A formação do Colégio de Presidentes da Maçonaria Brasileira.</h1>

            <p>
                Em 1973, a Maçonaria no Brasil passou por um processo de declaração conjunto de desfiliação de dez
                Grandes
                Orientes Estaduais do Grande Oriente do Brasil. Esse fato é considerado, junto com o momento histórico
                de 1927,
                um dos maiores acontecimentos da História da Maçonaria no Brasil, dando origem ao chamado “Colégio de
                Presidentes da Maçonaria Brasileira”. Iniciava-se uma nova jornada na Maçonaria brasileira e fixavam-se
                os
                alicerces para que outros Grandes Orientes Estaduais se orientassem pelos princípios federativos e
                constituíssem, primeiramente, um Colégio de Presidentes da Maçonaria brasileira, que evoluiu para a
                Confederação
                Maçônica do Brasil – COMAB [SOBRINHO, Octacílio Schüler Sobrinho: “Uma Luz na História: o sentido e a
                formação
                da COMAB”. Florianópolis: Ed. O PRUMO, 1998. p.299]. Por convocação do Grão-Mestre de Minas Gerais,
                Athos Vieira
                de Andrade, reuniram-se em Belo Horizonte/MG os Presidentes Danylo José Fernandes, do Grande Oriente de
                São
                Paulo, Osmar Maria Diógenes, do Grande Oriente do Ceará, Salatiel de Vasconcelos Silva, do Grande
                Oriente do Rio
                Grande do Norte, Celso Clarimundo da Fonseca, do Grande Oriente do Distrito Federal, Nilson Constantino,
                do
                Grande Oriente do Mato Grosso, Enoch Vieira dos Santos, do Grande Oriente do Paraná, Miguel Christakis,
                do
                Grande Oriente de Santa Catarina, Luiz Alberto de Alcântara Velho Barreto, do Grande Oriente de
                Pernambuco,
                Afonso Augusto de Morais, do Grande Oriente do Maranhão, e Ivan Neiva Neves, do Grande Oriente do
                Espírito
                Santo, resultando, dessa reunião, a fundação do Colégio de Presidentes da Maçonaria Brasileira. Em
                anúncio a
                todos os Grandes Orientes Estaduais, e a todas as Lojas, foi publicada a seguinte proclamação:
            </p>
