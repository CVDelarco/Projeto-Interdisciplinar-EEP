            <h1>Se a Maçonaria não é uma religião, porque se usa um ritual?</h1>
            <div class="subtexto">
                <p>
                    A relação entre ritual e religião é muito frequente, mas se analisarmos o assunto vamos notar que os
                    rituais
                    são uma parte de nós que pouco notamos. Ritual é simplesmente a maneira como algumas coisas são
                    feitas, uma espécie
                    de procedimento padrão para impor ordem e disciplina aos trabalhos. Uma reunião de condôminos
                    obedece a uma
                    ordem determinada, da mesma forma que uma reunião de pais e professores num colégio. Sem essa
                    sequência de atos
                    a serem vencidos, temos a indisciplina e a perda de tempo. O resultado será sempre questionável. Há
                    rituais
                    sociais ou convenções que nos dizem como participar de uma conversação, esperando por uma pausa,
                    como enfrentar
                    uma fila, com paciência, sem empurrar ou tentar passar à frente com algum tipo de artifício. A
                    Maçonaria usa um
                    ritual porque é um modo efetivo para ensinar ideias importantes. Além disso, o ritual Maçônico é
                    muito rico e
                    muito antigo, remontando aos primórdios de sua criação.
                </p>
            </div>

            <div class="texto">
                <h1>O que se aprende ao entrar para a Maçonaria?</h1>
                <div class="subtexto">
                    <p>Os maçons aprendem os seus preceitos em reuniões, que seguem liturgias, herdadas dos usos e
                        costumes dos
                        antigos (medievais) construtores de catedrais, utilizando como meio de transmissão dos seus
                        ensinamentos, os símbolos e
                        as alegorias dos antigos pedreiros. Entre outras coisas, nas Lojas maçônicas aprende-se a amar a
                        pátria em que
                        se vive, a se submeter às leis e às autoridades legalmente constituídas e considerar o trabalho
                        como um dever
                        essencial ao ser humano. A Maçonaria ensina e pratica os princípios e os ideais da decência,
                        honestidade,
                        gentileza, honradez, compreensão e afeto.</p>
                </div>
            </div>

            <h1>O que é um Templo Maçônico?</h1>
            <div class="subtexto">
                <p>Templo é o local onde se realizam as reuniões regulares das Lojas Maçónicas. Essas reuniões, nos seus
                    primórdios, não eram feitas em local específico. A partir de construção do Freemason’s hall, na
                    Inglaterra, em
                    1776, as reuniões ganharam um local fixo. Muitas Lojas, em função do tamanho de seu quadro, utilizam
                    as
                    instalações ou templos de outras lojas.</p>
            </div>

            <div class="texto">
                <h1>Os Maçons são Eletistas e/ou Racista?</h1>
                <div class="subtexto">
                    <p>Quanto ao racismo, a Maçonaria estabelece explicitamente a igualdade entre os homens sem
                        considerar raça,
                        credo ou cor. Se considerarmos que apenas são convidados a participar da Maçonaria homens
                        virtuosos e representantes
                        da sociedade, pode-se dizer que ela é uma elite, embora o correto seja afirmar que ela impõe
                        critérios rigorosos
                        para a iniciação de um novo membro. Costuma-se dizer que: o que no homem comum é uma virtude, no
                        maçom é uma
                        obrigação. Como se depreende, os critérios de seleção não se baseiam em valores materiais. A
                        Maçonaria congrega
                        uma ampla faixa de pessoas: profissionais liberais, comerciantes, professores, artistas,
                        políticos,
                        trabalhadores especializados, bancários, banqueiros, militares empresários, etc.. perante a
                        Maçonaria todos são
                        iguais.</p>
                </div>
            </div>

            <h1>Quem é o Chefe da Maçonaria?</h1>
            <div class="subtexto">
                <p>A Maçonaria não tem um chefe propriamente dito, mas cada Grande Loja é presidida por um oficial
                    denominado
                    Grão Mestre, eleito periodicamente pelo povo maçônico da jurisdição da própria Grande Loja. Dentro
                    da jurisdição da
                    sua Grande Loja, o Grão Mestre é a autoridade máxima.</p>
            </div>

            <div class="texto">
                <h1>Uma vez iniciado na Maçonaria, jamais podera sair?</h1>
                <div class="subtexto">
                    <p>Não há esse impedimento. Desejando afastar-se da Maçonaria, basta que o maçom requeira o seu
                        afastamento a
                        Loja, pois isso é um direito seu. A Maçonaria preza a liberdade dos seus membros tanto quanto
                        luta para preservar a
                        liberdade dos cidadãos em geral.</p>
                </div>
            </div>

            <h1>A Maçonaria é uma Religião?</h1>
            <div class="subtexto">
                <p>Não, a Maçonaria não é uma religião, mas defende a existência de um Ser Supremo ou princípio Criador.
                    Uma
                    religião é muito mais complexa, implicando em detalhes como a existência de um plano para salvação
                    ou caminho
                    pelo qual se alcança uma recompensa depois da vida terrena. Implica também numa teologia que tenta
                    descrever a
                    natureza de Deus e divulga a descrição de modos ou práticas pelos quais um homem ou uma mulher podem
                    buscar
                    comunicar-se com Deus. A Maçonaria não faz nenhuma dessas coisas. Apenas abre e fecha os seus
                    trabalhos com uma
                    oração e ensina que nenhum homem deveria começar qualquer empresa importante sem antes buscar apoio
                    espiritual
                    em Deus. Apesar disso, não ensina aos homens como eles devem rezar ou o que devem pedir. Ao invés
                    disso, prega
                    que cada um tem que achar as suas respostas para as suas grandes perguntas na sua própria fé, na sua
                    igreja,
                    sinagoga, templo ou qualquer outro templo religioso.</p>
            </div>

            <div class="texto">
                <h1>Quais são as exigências para se tornar Maçom?</h1>
                <div class="subtexto">
                    <p>É necessário que o candidato prima pela moral e pelos bons costumes.</p>
                </div>
            </div>

            <h1>Como se faz para se tornar Maçom?</h1>
            <div class="subtexto">
                <p>É preciso que o candidato seja indicado por um Mestre Maçom e tenha a sua iniciação aprovada pela
                    Loja. A
                    admissão é exclusiva para Homens. Um Maçom pode notar que alguém tem as qualidades que procuramos, e
                    o propõe
                    para admissão em uma de nossas Lojas. Caso venha a ser aceito pela Loja, a sua admissão será
                    providenciada.
                    Tenha paciência. Esse processo pode demorar algum tempo e nós precisamos ter certeza de que você
                    será um
                    elemento útil a nossa Instituição, assim como você também deverá ter certeza de que sua decisão será
                    benéfica
                    para você e sua família.</p>
            </div>

            <div class="texto">
                <h1>Um Religoso pode ser Maçom?</h1>
                <div class="subtexto">
                    <p>Nada impede que um religioso seja aceito como maçom. O que jamais se verá, no entanto, é um ateu
                        ser recebido
                        na Maçonaria regular, pois um dos princípios básicos para a admissão na Ordem é a crença em um
                        Ser Supremo.
                    </p>
                </div>
            </div>


            <h1>Os Maçons são Anti-Católicos?</h1>
            <div class="subtexto">
                <p>Em absoluto! Nada existe a esse respeito nas tradições e rituais maçônicos. Saliente-se que cada
                    maçom possui
                    as suas próprias convicções religiosas e todos convivem fraternalmente nas reuniões das suas Lojas e
                    fora delas. A
                    Maçonaria combate o sectarismo religioso em qualquer das suas manifestações e respeita a crença e a
                    profissão
                    religiosa dos seus membros.
                </p>
            </div>

            <div class="texto">
                <h1>Os rituais Maçônicos Criam embaraços ao Candidato?</h1>
                <div class="subtexto">
                    <p>Não. Os rituais em geral e os utilizados na cerimônia de iniciação foram escritos para sublinhar
                        virtudes que
                        deverão ser desenvolvidas pelos candidatos, tais como, Justiça, Amor Fraternal, Temperança,
                        Caridade,
                        Solidariedade, etc. Atualmente use-se nos rituais uma linguagem erudita e ricamente ilustrada
                        por simbolismo. Em
                        nenhum momento são criadas situações que possam embaraçar ou desagradar os candidatos, ou que
                        poderiam violar as
                        suas convicções patrióticas, crença religiosa ou familiar.
                    </p>
                </div>
            </div>

            <h1>A Maçonaria é uma socidedade secreta?</h1>
            <div class="subtexto">
                <p>Sociedades secretas geralmente são definidas como organizações desconhecidas do público e cuja
                    existência seja
                    escondida. A Maçonaria, por outro lado, é bem conhecida e orgulha-se de demonstrar a sua existência.
                    Os seus
                    Templos e as suas Lojas são facilmente identificados e muitos deles figuram nas listas telefônicas.
                    Muitos de
                    seus membros costumam usar anéis, distintivos de lapela, alfinetes de gravata que os identificam
                    como maçons.
                    Frequentemente os maçons participam ativamente junto à sua comunidade em trabalhos assistenciais.
                    Finalmente,
                    algumas atividades maçônicas são abertas e acessíveis ao público.
                </p>
            </div>

            <div class="texto">
                <h1>A Maçonaria não é apenas um clube de negócios?</h1>
                <div class="subtexto">
                    <p>Não. Dificilmente seria aprovado um candidato que quisesse entrar para a Maçonaria apenas por
                        interesses
                        comerciais. De qualquer forma, com o decorrer do tempo, os maçons vão criando grandes amizades
                        uns com os
                        outros, e não se deve estranhar quando algum negócio seja feito entre eles.
                    </p>
                </div>
