            <h1>O que é a Maçonaria?</h1>
            <div class="subtexto">
                <p>
                    A Maçonaria é uma organização fraternal que tem como princípio básico o amor fraterno, a prática da
                    caridade
                    e a busca da Verdade. Existe um velho ditado que diz: “A Maçonaria escolhe homens de bem e faz deles
                    ainda
                    melhores”. É normal dizer-se que os homens são o produto do meio que frequentam durante as suas
                    vidas. A
                    Maçonaria oferece a cada um dos seus membros a oportunidade de conviver regularmente com homens de
                    bom caráter,
                    o que reforça o seu próprio desenvolvimento pessoal e moral. A garantia dessa fraternal convivência
                    é conseguida
                    pela proibição de discussões político-partidárias ou religioso-sectárias, visto que esses assuntos,
                    têm dividido
                    os homens ao longo da história. Os maçons, sem discutir as suas crenças pessoais nestes dois
                    assuntos,
                    incentivam os homens a serem religiosos sem particularizar uma religião e encoraja-os a serem ativos
                    nas suas
                    comunidades, também sem particularizar o meio de expressão política. Os maçons, também conhecidos
                    como
                    “pedreiros livres”, não encontram na Maçonaria ensinamentos sobre a arte da construção, como o
                    faziam os maçons
                    operativos da Idade Média. Assim, as ferramentas comuns que eram usadas pelos canteiros medievais,
                    como o maço,
                    o cinzel, o nível, o prumo, o compasso e outros, têm cada uma um significado simbólico na Maçonaria.
                    A Maçonaria
                    distingue-se de outras ordens fraternais pela sua ênfase no caráter moral, no seu sistema de rituais
                    e na sua
                    longa tradição, com uma história que data aproximadamente do século XVII. Há três graus na
                    Maçonaria, Aprendiz,
                    Companheiro e Mestre. A maioria das lojas têm reuniões regulares e semanais e congregam-se em
                    potências
                    Maçônicas, chamadas Grandes Lojas ou Grandes Orientes.
                </p>
            </div>
